#include<stdio.h>

int main ()
{
  int no=11;

  char ch='A';

  printf("value of no: %d\n",no);

  printf("value of char: %c\n",ch);

  printf("address of no: %u\n",&no);

  printf("address of ch: %u\n",&ch);

  printf("sizeof no; %d\n",sizeof (no));

  printf("sizeof ch; %d\n",sizeof (ch));

  return 0;

} 